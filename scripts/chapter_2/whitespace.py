# tab text in with \t
print("\tHi, Xiaowei!")

# add new lines with \n
print(
    "Xiaowei has lived in:\nBoston\nMA"
)


# combine tab and new line
print(
    "Xiaowei has lived in:\n\tBoston\n\tMA"
)


# strip whitespace at the right end of a string with rstrip(), left end with lstrip(), or both with strip()
name = " xiaowei liu "
print(name)
print(name.title().rstrip())
print(name.title().lstrip())
print(name.title().strip())

